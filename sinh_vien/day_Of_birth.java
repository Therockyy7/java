



public class day_Of_birth {
    int day, month, year;
    public day_Of_birth(int day, int month, int year){
        this.day = day;
        this.month = month;
        this.year = year;
    }
    
    public int getDay(){
        return day;
    }
    public void setDay(int dy){
        this.day = dy;
    }

    public int getMonth(){
        return month;
    }
    public void setMonth(int mth){
        this.month = mth;
    }

    public int getYear(){
        return year;
    }
    public void setYear(int yr){
        this.year = yr;
    }


}
